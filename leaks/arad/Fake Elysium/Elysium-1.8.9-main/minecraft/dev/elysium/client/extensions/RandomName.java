	public static String getRandomName() {
		String name = "";
		if(Math.random() < 0.2) {
			name = name.concat("_");
		}
		if(Math.random() >= 0.3 && Math.random() < 0.5) {
			name = name.concat("X");
		}
		name = name.concat(first_names[(int) Math.floor(Math.random() * first_names.length)]);
		name = name.concat(String.valueOf(Math.floor(Math.random() * 99)));
		
		name = name.substring(0, name.length() - 2);
		
		if(Math.random() >= 0.1 && Math.random() < 0.5) {
			name = name.concat("X");
		}
		
		return name;
	}
